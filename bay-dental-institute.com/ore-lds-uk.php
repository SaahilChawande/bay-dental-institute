                                                                <span class="sc_accordion_icon sc_accordion_icon_opened icon-minus"></span> If I am a MDS / Specialist Abroad can I register as one with the GDC </h5>
                                                            <div class="sc_accordion_content">
                                                                <div class="wpb_text_column wpb_content_element ">
                                                                    <div class="wpb_wrapper">
                                                                        <p>Yes. If you are a specialist in your home country, after you have passed the ORE and registered with the GDC, you can also register with your MDS / Specialist Degree if it is from a recognized institution. However while applying for the ORE they only require your BDS degree.  </p>


                                                                    </div>
