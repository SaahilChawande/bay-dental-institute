                                                                <span class="sc_accordion_icon sc_accordion_icon_closed icon-plus"></span>
                                                                <span class="sc_accordion_icon sc_accordion_icon_opened icon-minus"></span> What if I have passed the exam but don’t wish to move to the UK immediately ?</h5>
                                                            <div class="sc_accordion_content">
                                                                <div class="wpb_text_column wpb_content_element ">
                                                                    <div class="wpb_wrapper">
                                                                        <p>Once you pass the ORE. You can choose to register immediately with the GDC or whenever you are ready to move.
                                                                            You can also register for that particular year and receive you GDC registration number and certificate. The Following year if you wish to not move, or pay the Annual Retention Fee ( ARF ), you can write to the GDC of the same, they will take you off the register, and you do not have to pay the ARF. At any point in the future if you wish to move, you can simply pay the fees for that year and go back onto the register. You will not be required to take the ORE again or pay any Fine. You will have to ensure your CPD hours are ongoing during your time off the register.
                                                                        </p>


                                                                    </div>
