                                                                <div class="wpb_text_column wpb_content_element ">
                                                                    <div class="wpb_wrapper">
                                                                        <p>Once you have cleared Part II, you will receive as email stating you have passed and are now entitled to register as a dentist with the GDC. You will be required to resend a few documents to the GDC along with a form. Following processing you will be required to pay the annual fee for registration which is 800GBP. Also known as the Annual Retention Fee (ARF ) . The amount of ARF you pay depends on the month you register. For example if you want to register in june, your ARF will be 50% i.e 400GBP. </p>


                                                                    </div>
                                                                </div>
                                                            </div>
                                                        </div>
