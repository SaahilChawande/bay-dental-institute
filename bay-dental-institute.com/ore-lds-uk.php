                                                                <span class="sc_accordion_icon sc_accordion_icon_closed icon-plus"></span>
                                                                <span class="sc_accordion_icon sc_accordion_icon_opened icon-minus"></span>  What is a VT ?</h5>
                                                            <div class="sc_accordion_content">
                                                                <div class="wpb_text_column wpb_content_element ">
                                                                    <div class="wpb_wrapper">
                                                                        <p>After passing the ORE, you have an option to work in a 100% private practice or a NHS hospital / practice. If you are able to secure a job contract in a 100% private practice you do not need a VT.  To work with the NHS you require something known as a performer number. This performer number can be obtained only after you have undergone something known as a Vocational Training ( VT ). Vocational Training where you are part of a NHS practice where they overlook and train you in the workings of the NHS after which you receive this performer number. Once you receive this performer number you are free to seek a job in any NHS practice, hospital across the UK. </p>


                                                                    </div>
