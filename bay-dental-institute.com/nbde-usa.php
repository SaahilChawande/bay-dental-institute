                                                       National Board Dental Examination (NBDE) is the United States national dental examination for students and professionals in dentistry. It is required for licensure in the United States and may also be required when applying for postgraduate studies in dental specialties after completing a dental degree. Foreign-trained dentists ( E.g. Indian Dentists) also must take the NBDE in order to earn admission into advanced standing programs in US dental schools.
													</p>
