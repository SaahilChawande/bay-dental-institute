														NBDE I consists of 400 multiple choice questions emphasizing basic sciences: <br/>
														1. Human Anatomy, Embryology, and Histology <br/>
														2. Biochemistry and Physiology <br/>
														3. Microbiology and Pathology <br/>
														4. Dental Anatomy and Occlusion.<br/><br/>

                                                    
                                                   
                                                       NBDE II requires two days and focuses on clinical dental topics:<br/>
														1. Endodontics<br/>
														2. Operative Dentistry<br/>
														3. Oral and Maxillofacial Surgery/Pain Control <br/>
														4. Oral Diagnosis <br/>
														5. Orthodontics and Pediatric Dentistry <br/>
														6. Patient Management, including Behavioral Science, Dental Public Health and Occupational Safety <br/>
														7. Periodontics <br/>
